# Integer, Float, Character, String, Boolean


# Integer DataType
age = str(28)
print("Age is: " + age)

# Float Data
height = str(5.4)
print("height is: " + height)

# Character DataType
symbol = 'h'
print("The Symbol is: " + symbol)


# String DataType
name = "Jawad"
print("The name is: " + name)

# Boolean DataType
is_true = str(True)
print("Boolean Value: "+ is_true)